export const formatPrice = (price: number, transaction: 'vente' | 'location'): string => {
  if (transaction === 'location') {
    return `${price.toLocaleString('fr-DZ')} DA/mois`
  }
  if (price >= 1000000000) {
    return `${(price / 1000000000).toFixed(2)} Milliard DA`
  }
  if (price >= 1000000) {
    return `${(price / 1000000).toFixed(0)} Millions DA`
  }
  return `${price.toLocaleString('fr-DZ')} DA`
}